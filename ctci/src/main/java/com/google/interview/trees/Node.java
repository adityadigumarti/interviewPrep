package com.google.interview.trees;

public class Node<T> {

	private T data;
	private Node<T> left;
	private Node<T> right;
	
	
	public Node<T> left() {
		return left;
	}
	
	public Node<T> right() {
		return right;
	}
	
	public void setLeft(Node<T> node) {
		this.left = left;
	}
	
	public void setRight(Node<T> node) {
		this.left = left;
	}
	
	

}
